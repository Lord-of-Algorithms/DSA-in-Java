package graph.traversal.main;

/**
 * Enumerates the supported graph traversal methods.
 */
enum TraversalMethod {
    /**
     * Depth-First Search traversal method using an iterative approach with a stack.
     */
    DFS,
    /**
     * Depth-First Search traversal method using a recursive approach.
     */
    DFS_RECURSIVE,
    /**
     * Breadth-First Search traversal method.
     */
    BFS
}
